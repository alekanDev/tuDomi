export const colorList = (color) => {
  switch (color) {
    case 'GREEN1_1':
      return '#BEF264'
    case 'BLACK1_1':
      return '#171717'
    case 'GRAY1_1':
      return '#404040'
    case 'GRAY2_1':
      return '#525252'
    case 'GRAY_LIGHT':
      return '#A3A3A3'
    case 'ORANGE1_1':
      return '#FFB347'
    case 'RED1_1':
      return '#FA7890'
    case 'BLUE1_1':
      return '#688DB9'
    case 'WHITE':
      return '#FFFFFF'
    default:
      return 'yellow'
      
  }
}